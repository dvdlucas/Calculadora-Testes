
package calculadora;

public class Calculadora {
    protected int Soma (int a, int b){
        return a + b; //Metodo para Somar Dois Valores
    }
    
    protected int Subtrair(int a, int b){
        return a - b; //Metodo para Subtrair um valor de outro
    }
    
    protected int Divisao(int a, int b){
        return a / b;
    }
    
    protected int Multiplicacao(int a, int b){
        return a * b;
    }
}
